#include "libft.h"

int	ft_isspace(char d)
{
	char	c;

	c = (unsigned char) d;
	if (c == ' ' || c == '\f' || c == '\t' || c == '\n'
		|| c == '\r' || c == '\v')
		return (1);
	return (0);
}
